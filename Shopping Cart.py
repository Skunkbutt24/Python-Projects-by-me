print("Welcome to my virtual shopping cart program. Let's get started.")
products = []
prices = []
total_price = 0
number_of_items=0
while True:
    product = input("Enter the name of product you want to store in cart: ").lower()
    products.append(product)
    price = float(input(f"Enter the price of {product.capitalize()}: $ "))
    prices.append(price)
    print(f'Product "{product.capitalize()}" has been added to your cart for {price:.2f}$')
    continue_adding = input("Do you want to add more stuffs ?\n").lower()
    while continue_adding not in ["yes","no"]:
        continue_adding=input("Invalid input. Try again.\nDo you want to add more stuffs ?\n").lower()
    if continue_adding == "no":
        break
print("So, your shopping cart contains: ")
print("===========================================\n")
n=1
for product,price in zip(products,prices):
    print(f"{n}.{product.capitalize()}: {price:.2f}$")
    total_price += price
    number_of_items+=1
    n+=1
print(f"\nNumber of items in cart: {number_of_items}")
print("Tax percentage: 4%")
print(f"Total price with tax: ${(total_price+(total_price*(4/100))):.2f}")
